    public String comboPosicion(){
        if(jComboCurso.getSelectedIndex()==0){
            String dam1="DAM 1";
            return dam1;
        }
        if(jComboCurso.getSelectedIndex()==1){
            String dam2="DAM 2";
            return dam2;
        }
        if(jComboCurso.getSelectedIndex()==2){
            String asir1="ASIR 1";
            return asir1;
        }
        if(jComboCurso.getSelectedIndex()==3){
            String asir2="ASIR 2";
            return asir2;
        }
        return null;
    }
    
    
    
