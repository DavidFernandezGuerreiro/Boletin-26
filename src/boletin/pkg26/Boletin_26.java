    private void bAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAgregarActionPerformed
        //DefaultTableModel se utiiza para almacenar objetos de valor de celda. 
        DefaultTableModel modelo=(DefaultTableModel) jTable.getModel(); 
        //Array donde guardamos los datos de los componentes.
        Object[]fila=new Object[6]; 
        //Recogemos los datos en orden.
        fila[0]=jTextNome.getText(); 
        fila[1]=jTextApelido.getText(); 
        fila[2]=comboPosicion(); //<--(después me dí cuenta que podía coger los datos con el método getSelectedItem();)
         //Agregamos el Array al modelo de la tabla.
         modelo.addRow(fila); 
        //Agregamos el modelo a nuestra tabla.
        jTable.setModel(modelo); 
 
    }//GEN-LAST:event_bAgregarActionPerformed

    
